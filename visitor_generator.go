package mkunion

import (
	"bytes"
	_ "embed"
	"text/template"
)

const (
	Program = "mkunion"
	Header  = `// Code generated by ` + Program + `. DO NOT EDIT.`
)

type Generator interface {
	Generate() ([]byte, error)
}

var (
	//go:embed visitor_generator.go.tmpl
	visitorTmpl string
	render      = template.Must(template.New("main").Parse(visitorTmpl))
)

type VisitorGenerator struct {
	Header      string
	Types       []string
	Name        string
	PackageName string
}

func (g *VisitorGenerator) Generate() ([]byte, error) {
	result := &bytes.Buffer{}
	err := render.ExecuteTemplate(result, "main", g)
	if err != nil {
		return nil, err
	}

	return result.Bytes(), nil
}
